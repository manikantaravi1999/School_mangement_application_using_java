package com.in.studentinformation;

public class SchoolAdminLogin {
	private String SCHOOL_ID;
	private String SCHOOL_PASSWORD;

	/*
	 * school secret id and password setters and getters
	 */
	public String getSchool_Id() {
		return SCHOOL_ID;
	}

	public void setSchool_Id(String school_Id) {
		SCHOOL_ID = school_Id;
	}

	public String getSchool_password() {
		return SCHOOL_PASSWORD;
	}

	public void setSchool_password(String school_password) {
		SCHOOL_PASSWORD = school_password;
	}

}
